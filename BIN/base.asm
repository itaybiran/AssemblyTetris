
;גיבוי רגיסטרים
macro PushAll 
	push ax
	push bx
	push cx
	push dx
	push di
	push si
endm PushAll



;=============================================================================================================================================================



;החזרת ערכי הרגיסטרים מגיבוי
macro PopAll 
	pop si
	pop di
	pop dx
	pop cx
	pop bx
	pop ax
endm PopAll



;=============================================================================================================================================================



;פרוצדורה שמתחילה את התכנית
proc begin
	mov ax, @data
	mov ds, ax
	ret
endp begin



;=============================================================================================================================================================



;פרוצדורה שיוצאת מהתכנית
proc escape
	push ax
	mov ax, 4c00h
	int 21h
	pop ax
	ret
endp escape



