
;פותח רמקול
proc openSpeaker
	in al, 61h
	or al, 00000011b
	out 61h, al
	ret
endp openSpeaker



;=============================================================================================================================================================



;סוגר רמקול
proc closeSpeaker
	in al, 61h
	and al, 11111100b
	out 61h, al
	ret
endp closeSpeaker



;=============================================================================================================================================================


;מנגן את התו דו
proc playDo
	mov al, 0dbh
	out 42h, al
	mov al, 11h
	out 42h, al
	ret
endp playDo



;=============================================================================================================================================================



;מנגן את התו רה
proc playRe
	mov al, 0DAh
	out 42h, al
	mov al, 0Fh
	out 42h, al
	ret
endp playRe



;=============================================================================================================================================================



;מנגן את התו לה
proc playLa
	mov al, 98h
	out 42h, al
